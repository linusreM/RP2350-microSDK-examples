#define LED_MATRIX_MASK (0x7FF)

void display_led_matrix(uint8_t row, uint8_t data){
    data = (data & 0x7) | 
           ((data & (1U << 3))<<4) |
           ((data & (1U << 4))<<2) |
           ((data & (1U << 5))>>2) |
           ((data & (1U << 6))>>1) |
           ((data & (1U << 7))>>3); 

    gpio_write(LED_MATRIX_MASK, (data << 3) | row);
}

int main(void){
    //Configure LED Matrix outputs
    for(int i = 0; i < 11; i++) gpio_config(i, 1);
    
    //Elapsed miliseconds
    uint32_t ms = 0;
    //Current row to display
    uint8_t row = 0;
    //What part of the bit pattern will be displayed
    uint32_t pattern_offset = 0;
    while(1){
        //Wait for time interval
        while(system_timer_read(0) < (ms * 1000));
        ms++;
        //Setup next interval
        if((ms % 200) == 0) pattern_offset = (pattern_offset + 1) % sizeof(pattern);

        display_led_matrix(row, pattern[(pattern_offset + row) % sizeof(pattern)]);
        row = (row + 1) % 8;
    }
    
}